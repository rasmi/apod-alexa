# --------------- Functions that control the skill's behavior ------------------

def get_explanation():
    url = 'http://apod.nasa.gov/apod/astropix.html'
    request = urllib2.urlopen(url)
    html = request.read()
    html = html.replace('\n',' ')
    explanation_html = html.split('<b> Explanation: </b>')[1]
    explanation_html = explanation_html.split("<p> <center>")[0]
    explanation = re.sub(r'<.*?>','', explanation_html)
    explanation = re.sub(r'\s+',' ', explanation)

    return explanation

def explanation_response():
    session_attributes = {}
    title = 'APOD'
    output = get_explanation()
    return build_response(session_attributes, build_speechlet_response(title, output))

# ------------------ Helper functions to build the response --------------------

def build_speechlet_response(title, output):
    return {
        'outputSpeech': {
            'type': 'PlainText',
            'text': output
        },
        'card': {
            'type': 'Simple',
            'title': title,
            'content': output
        },
        'shouldEndSession': True
    }

def build_response(session_attributes, speechlet_response):
    return {
        'version': '1.0',
        'sessionAttributes': session_attributes,
        'response': speechlet_response
    }